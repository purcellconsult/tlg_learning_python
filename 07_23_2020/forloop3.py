
# keep count of the vowels
vowel = 0 # the counter
basket = input('Enter in some text: ') # reads input from user
for item in basket:
    if item == 'a' or item == 'e' or item == 'i' or item == 'o' or item == 'u':
        vowel += 1

print('In the string: \n  {}'.format(basket))
print('The vowel count is {}'.format(vowel))





